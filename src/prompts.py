    "    {\n"
    "      \"action\": \"create_file\",\n"
    "      \"input\": \"notes.txt\"\n"
    "    }\n"
    "  ],\n"
    "  \"response_mode\": \"RAW\",\n"
    "  \"target_source\": \"EXECUTOR\",\n"
